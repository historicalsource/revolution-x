SYNC_STEVEN_W
	MOVE	*A8(OPART1),A8,L
	MOVE	@LAST_RX,A3
	MOVE	A3,A4
	MOVE	@SOUND_RX,A1
	MOVE	A1,A5
	SLL	28,A5
	SLL	28,A3
	CMP	A3,A5
	JREQ	IGNORE_NEW_ANIM_W
	SLL	24,A4
	SRL	25,A4
	SLL	2,A4
	ADDI	WHICH_SET_STEVEN_W-020h,A4
	MOVE	*A4,A4,L
	MOVE	@BEAT_COUNT,A5
	SLL	5,A5
	ADD	A5,A4
	MOVE	*A4,A4,L
	JRZ	CHECK_IF_WE_ARE_BOUNCING_W

	MOVE	A4,*A8(AnimFrm),L
	MOVE	A4,*A8(AnimScr),L
	CLR	A4
	MOVE	A4,*A8(AnimSLP)
